//TIPOS DE FUNÇÕES

// DECLARATIVAS
function funcao() {
    console.log('Função declarativa');
}
funcao();

// EXPRESSÃO DE FUNÇÃO
var funcao2 = function() {
    console.log('expressão de função');
}

funcao2();

//ARROW FUNCTION
var funcao3 = () => {
    console.log('arraw function');
}

funcao3();

